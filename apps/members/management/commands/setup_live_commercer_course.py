"""
ライブコマーサー育成講座 セットアップコマンド

MemberSite, ファネルLP x4, ステップメールシナリオ x4 を一括作成する。
冪等: 同じslugが既に存在する場合はスキップする。

Usage:
    python manage.py setup_live_commercer_course --project-id=1
"""
from django.core.management.base import BaseCommand, CommandError
from apps.accounts.models import Project
from apps.members.models import MemberSite
from apps.funnels.models import Funnel, FunnelPage, PageSection
from apps.emails.models import Scenario, ScenarioStep


class Command(BaseCommand):
    help = 'ライブコマーサー育成講座のファネルLP・ステップメールを一括セットアップ'

    def add_arguments(self, parser):
        parser.add_argument('--project-id', type=int, help='Project ID')

    def handle(self, *args, **options):
        project = self._get_project(options)
        self.stdout.write(f'Project: {project.name} (ID={project.id})')

        site = self._create_member_site(project)
        funnels = self._create_funnels(project, site)
        scenarios = self._create_scenarios(project, site)

        # LP①のフォームにシナリオAのIDを設定
        self._link_form_to_scenario(funnels, scenarios)

        self.stdout.write(self.style.SUCCESS('セットアップ完了'))

    # ------------------------------------------------------------------
    # helpers
    # ------------------------------------------------------------------

    def _get_project(self, options):
        pid = options.get('project_id')
        if pid:
            try:
                return Project.objects.get(id=pid)
            except Project.DoesNotExist:
                raise CommandError(f'Project ID={pid} が見つかりません')
        project = Project.objects.first()
        if not project:
            raise CommandError('Projectが1つもありません。先にProjectを作成してください。')
        return project

    # ------------------------------------------------------------------
    # 1. MemberSite
    # ------------------------------------------------------------------

    def _create_member_site(self, project):
        site, created = MemberSite.objects.get_or_create(
            slug='live-commercer-course',
            defaults={
                'project': project,
                'name': 'ライブコマーサー育成講座',
                'description': 'ライブコマース配信の型・安全設計・運用を段階的に学ぶ育成講座',
                'is_active': True,
            },
        )
        self.stdout.write(f'  MemberSite: {"作成" if created else "既存"} - {site.name}')
        return site

    # ------------------------------------------------------------------
    # 2. Funnels + Pages + Sections
    # ------------------------------------------------------------------

    def _create_funnels(self, project, site):
        funnels = {}
        for funnel_def in FUNNEL_DATA:
            funnel, created = Funnel.objects.get_or_create(
                project=project,
                slug=funnel_def['slug'],
                defaults={
                    'name': funnel_def['name'],
                    'site': site,
                    'is_published': True,
                },
            )
            self.stdout.write(f'  Funnel: {"作成" if created else "既存"} - {funnel.name}')
            funnels[funnel_def['slug']] = funnel

            if created:
                for page_def in funnel_def['pages']:
                    page = FunnelPage.objects.create(
                        funnel=funnel,
                        title=page_def['title'],
                        slug=page_def['slug'],
                        page_type=page_def['page_type'],
                        sort_order=page_def.get('sort_order', 0),
                        meta_title=page_def.get('meta_title', page_def['title']),
                        meta_description=page_def.get('meta_description', ''),
                    )
                    for i, section in enumerate(page_def['sections']):
                        PageSection.objects.create(
                            page=page,
                            section_type=section['type'],
                            content=section['content'],
                            sort_order=i,
                            is_visible=True,
                        )
                    self.stdout.write(f'    Page: {page.title} ({len(page_def["sections"])} sections)')
        return funnels

    # ------------------------------------------------------------------
    # 3. Scenarios + Steps
    # ------------------------------------------------------------------

    def _create_scenarios(self, project, site):
        scenarios = {}
        for sc_def in SCENARIO_DATA:
            scenario, created = Scenario.objects.get_or_create(
                project=project,
                name=sc_def['name'],
                defaults={
                    'site': site,
                    'is_active': True,
                },
            )
            self.stdout.write(f'  Scenario: {"作成" if created else "既存"} - {scenario.name}')
            scenarios[sc_def['key']] = scenario

            if created:
                for step_def in sc_def['steps']:
                    ScenarioStep.objects.create(
                        scenario=scenario,
                        step_number=step_def['step_number'],
                        delay_days=step_def['delay_days'],
                        delay_hours=step_def.get('delay_hours', 0),
                        subject=step_def['subject'],
                        body_html=step_def['body_html'],
                        body_text=step_def.get('body_text', ''),
                        is_active=True,
                    )
                self.stdout.write(f'    Steps: {len(sc_def["steps"])} 通作成')
        return scenarios

    # ------------------------------------------------------------------
    # 4. LP① form → Scenario A
    # ------------------------------------------------------------------

    def _link_form_to_scenario(self, funnels, scenarios):
        funnel = funnels.get('lc-stage1')
        scenario = scenarios.get('A')
        if not funnel or not scenario:
            return
        form_sections = PageSection.objects.filter(
            page__funnel=funnel,
            section_type='form',
        )
        updated = 0
        for section in form_sections:
            content = section.content or {}
            if content.get('scenario_id') != scenario.id:
                content['scenario_id'] = scenario.id
                section.content = content
                section.save(update_fields=['content'])
                updated += 1
        if updated:
            self.stdout.write(f'  Form→Scenario連携: {updated} セクション更新')


