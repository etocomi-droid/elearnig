def _email_html(body_text):
    """プレーンテキストを簡易HTMLメールに変換"""
    paragraphs = body_text.strip().split('\n\n')
    html_parts = []
    for p in paragraphs:
        p = p.strip()
        if p.startswith('・') or p.startswith('•'):
            items = p.split('\n')
            html_parts.append('<ul>')
            for item in items:
                item = item.lstrip('・• ').strip()
                if item:
                    html_parts.append(f'<li>{item}</li>')
            html_parts.append('</ul>')
        elif p.startswith('▼') or p.startswith('【CTA】'):
            html_parts.append(f'<p><strong>{p}</strong></p>')
        else:
            lines = p.split('\n')
            html_parts.append('<p>' + '<br>'.join(lines) + '</p>')
    return '\n'.join(html_parts)


