def _html(text):
    """簡易ヘルパー: 改行を<br>に、段落を<p>に変換"""
    paragraphs = text.strip().split('\n\n')
    parts = []
    for p in paragraphs:
        lines = p.strip().split('\n')
        parts.append('<p>' + '<br>'.join(lines) + '</p>')
    return '\n'.join(parts)


